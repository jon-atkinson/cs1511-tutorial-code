// The program prints two simple faces
//
// Face 1:
//  ~ ~
//  0 0
//   o
//   -
//
// Face 2:
//  ~ ~
//  0 0
//   o
//  \_/


#include <stdio.h>

int main(void) {
    // first face
    printf("~ ~\n");
    printf("0 0\n");
    printf(" o\n");
    printf(" -\n\n");

    // second face
    printf("~ ~\n");
    printf("0 0\n");
    printf(" o\n");
    printf("\\_/\n");

    return 0;
}
